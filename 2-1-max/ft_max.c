int		ft_max(int *tab, unsigned int len)
{
	int		tmp;
	int		i;

	tmp = tab[0];
	while (i < len)
	{
		if (tmp < tab[i])
			tmp = tab[i];
		i++;
	}
	return (tmp);
}
